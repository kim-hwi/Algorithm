	LDB	LENGTH	;3
	BASE	LENGTH
	CLEAR	A	;2
	CLEAR	X	;2
	COMP	=2048	;3
	JEQ	FINISH	;3
LOOP	+LDCH	BUFONE,X	
	RMO	A,T
	+LDCH	BUFTWO,X
	RMO	A,S
	COMPR	T, S
	JLT	JMPONE
	JGT	JMPTWO
	TIX	=2048
	JLT	LOOP
